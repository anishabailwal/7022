// C++ Program to convert int to char

#include <iostream>

using namespace std;

int main() {
    int yes = 51; // ASCII code for '3'
    char yes_char = (char)yes;

    cout << "The integer value of 'yes' is: " << yes << endl;
    cout << "The character value of 51 is: " << yes_char << endl;

    return 0;
}
